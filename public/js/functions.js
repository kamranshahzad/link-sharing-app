function callSubStates($this){
		if($this.checked){
			$data = 'state='+$this.value ;
			$.ajax({
				url: "../app/ajax/LookingAdmFrm.php",  
				type: "GET",        
				data: $data,     
				//cache: false,
				success: function (html) {  
					$('#'+$this.value ).html(html);     
				}       
			});
		}else{
			$('#'+$this.value ).html(''); 
		}
} //callSubStates


function populateCounties($this){
	$data = 'state='+$this.value ;
		$.ajax({
			url: "../app/ajax/BusinessCountiesFrm.php",  
			type: "GET",        
			data: $data,     
			//cache: false,
			success: function (html) {  
				$('#bcounty' ).html(html);     
			}       
	});
}



